    private void cadastrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarButtonActionPerformed
        try {
            _usuariosController.Cadastrar(textUsuario.getText(), textSenha.getText());
        } catch (RemoteException ex) {
            Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            _erroView.SetMessage(ex.getMessage());
            _erroView.setVisible(true);
        }
    }//GEN-LAST:event_cadastrarButtonActionPerformed

    private void entrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarButtonActionPerformed
        try {
            
            _clienteCallback = new ClienteCallback(this, textUsuario.getText());
            
            _usuariosController.Entrar(textUsuario.getText(), textSenha.getText(), _clienteCallback);
            
            cadastrarButton.setVisible(false);
            entrarButton.setVisible(false);
            sairButton.setVisible(true);
            painelLogado.setVisible(true);
        } catch (RemoteException ex) {
            Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            _erroView.SetMessage(ex.getMessage());
            _erroView.setVisible(true);
        }
    }//GEN-LAST:event_entrarButtonActionPerformed

    private void sairButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sairButtonActionPerformed
        
        try {
            _usuariosController.Sair(textUsuario.getText(), _clienteCallback);
            cadastrarButton.setVisible(true);
            entrarButton.setVisible(true);
            sairButton.setVisible(false);
            painelLogado.setVisible(false);

        } catch (Exception ex) {
            Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_sairButtonActionPerformed

