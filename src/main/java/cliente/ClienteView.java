    public void UpdateUsuarios(List<Usuario> usuarios){
        _usuariosListModel.clear();
        if(usuarios != null){
            for(Usuario usuario : usuarios){
                if(usuario.GetOnline()){
                    _usuariosListModel.addElement(usuario.GetName() + " (online)");
                }
                else{
                    _usuariosListModel.addElement(usuario.GetName());
                }
            }
        }
    }

